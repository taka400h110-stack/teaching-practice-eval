export type RdLevel = "RD0" | "RD1" | "RD2" | "RD3" | "RD4";

/** スコアから RD水準オブジェクトを取得 */
export function getRdByScore(score: number) {
  return REFLECTION_DEPTH_LEVELS.find((r) => r.score === score) ?? REFLECTION_DEPTH_LEVELS[0];
}

