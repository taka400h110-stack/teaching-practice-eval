export function getBehaviorIndicator(itemNum: number, score: number): string {
  const item = getItemByNum(itemNum);
  if (!item) return "";
  const behavior = item.behaviors.find((b) => b.score === score);
  return behavior?.indicator ?? "";
}

/** スコアから RD水準ラベルを取得 */
export function getRdLabelByScore(score: number): string {
  return getRdByScore(score).label;
}

