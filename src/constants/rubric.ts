/** 因子キーから因子オブジェクトを取得 */
export function getFactorByKey(key: FactorKey) {
  return RUBRIC_FACTORS.find((f) => f.key === key)!;
}

/** 項目番号から属する因子キーを取得 */
export function getFactorKeyByItemNum(num: number): FactorKey {
  for (const f of RUBRIC_FACTORS) {
    if (num >= f.itemRange[0] && num <= f.itemRange[1]) return f.key;
  }
  return "factor1";
}

/** 因子キーに属する項目一覧を取得 */
export function getItemsByFactor(key: FactorKey) {
  return RUBRIC_ITEMS.filter((item) => item.factor === key);
}

/** 項目番号から項目オブジェクトを取得 */
export function getItemByNum(num: number): RubricItem | undefined {
  return RUBRIC_ITEMS.find((item) => item.num === num);
}

/** スコアから RD水準の行動指標を取得（特定項目） */
export function getBehaviorIndicator(itemNum: number, score: number): string {
  const item = getItemByNum(itemNum);
  if (!item) return "";
  const behavior = item.behaviors.find((b) => b.score === score);
  return behavior?.indicator ?? "";
}

/** スコアから RD水準ラベルを取得 */
export function getRdLabelByScore(score: number): string {
  return getRdByScore(score).label;
}

